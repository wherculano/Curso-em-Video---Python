n = input ('Digite algo ')
print('\nTipo primitivo? ',type(n))
print('Numérico? ',n.isnumeric())
print('Letra? ',n.isalpha())
print('Alfanumérico? ',n.isalnum())
print('Tudo Maiúsculo? ',n.isupper())
print('Tudo minúsculo? ',n.islower())
print('Impresso? ',n.isprintable())
print('Só espaço? ',n.isspace())
print('Digito? ',n.isdigit())
print('Decimal? ',n.isdecimal())
print('Título? ',n.istitle())
print('Identificável? ',n.isidentifier())





